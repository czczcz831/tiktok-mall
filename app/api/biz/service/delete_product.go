type DeleteProductService struct {
	RequestContext *app.RequestContext
	Context        context.Context
}

func NewDeleteProductService(Context context.Context, RequestContext *app.RequestContext) *DeleteProductService {
	return &DeleteProductService{RequestContext: RequestContext, Context: Context}
}

func (h *DeleteProductService) Run(req *api.DeleteProductReq) (resp *api.DeleteProductResp, err error) {
	//defer func() {
	// hlog.CtxInfof(h.Context, "req = %+v", req)
	// hlog.CtxInfof(h.Context, "resp = %+v", resp)
	//}()
	// todo edit your code

	deleteResp, err := productAgent.DeleteProduct(h.Context, &product.DeleteProductReq{
		Uuid: req.UUID,
	})

	if err != nil {
		return nil, err
	}

	return &api.DeleteProductResp{
		UUID: deleteResp.Uuid,
	}, nil
}
